vlli pos[2];
vector<vlli> dp; 

lli f(int o, int z) {
    if (o >= pos[1].size()) return 0;
    if (z >= pos[0].size()) return INF;
    if (dp[o][z] != -1) return dp[o][z];
    // crossing is bad
    dp[o][z] = min(f(o+1,z+1) + abs(pos[1][o]-pos[0][z]), f(o,z+1));
    return dp[o][z];
}

void solve() {
    int n; cin >> n;
    vlli a(n); for (auto &i: a) cin >> i;
    for (lli i = 0; i < n; i++) pos[a[i]].pb(i);
    dp = vector<vlli>(pos[1].size(), vlli(pos[0].size(), -1));
    cout << f(0,0) << endl;
}   
  
