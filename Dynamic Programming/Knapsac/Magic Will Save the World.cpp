void solve() {
    lli w, f, n; cin >> w >> f >> n;
    lli sum = 0;
    vlli a(n+1); for (int i = 1; i <= n; i++) cin >> a[i], sum += a[i];
    
    vector<vi> dp(2, vi(sum + 1));
    dp[0][0] = 1;
    for (lli i = 1; i <= n; i++) {
        for (lli j = 0; j <= sum; j++) {
            dp[1][j] = dp[0][j];
            if (j-a[i] >= 0) dp[1][j] = dp[1][j] || dp[0][j-a[i]];
        }
        dp[0] = dp[1];
    }
    lli ans = 2e9;
    for (int i = 0; i <= sum; ++i) {
        if (dp[1][i]) {
            ans = min(ans, max((i + w - 1) / w, (sum - i + f - 1) / f));
        }
    }
    cout << ans << "\n";
}
  
