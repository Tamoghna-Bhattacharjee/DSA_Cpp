// Finding a pattern inside a text using KMP algo

vi getLPS(string &s) {
    int n = s.size();
    int i = 1, j = 0;
    vi LPS(n);
    while (i < n) {
        if (s[i] == s[j]) LPS[i++] = ++j;
        else if (j == 0) i++;
        else j = LPS[j-1];
    }
    return LPS;
}

bool isSubstr(string &t, string &p, vi &LPS) {
    int n = t.size(), m = p.size();
    int i = 0, j = 0;
    while (i < n && j < m) {
        if (t[i] == p[j]) {
            i++; j++;
            continue;
        } else {
            if (j == 0) i++;
            else j = LPS[j-1];
        }
    }
    return j == m;
}

void solve() {
    string pattern, text; cin >> pattern >> text;
    vi LPS = getLPS(pattern);
    cout << YN[isSubstr(text, pattern, LPS)] << endl;
}
  
