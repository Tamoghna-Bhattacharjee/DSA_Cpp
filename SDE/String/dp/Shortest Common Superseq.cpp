void solve() {
    string s, t; cin >> s >> t;
    int n = s.size(), m = t.size();
    s = "#" + s; t = "#" + t;
    int dp[n+1][m+1]; memset(dp, 0, sizeof dp);
    int mx = 0, end = 0;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if (s[i] == t[j]) dp[i][j] = 1 + dp[i-1][j-1];
            else dp[i][j] = max(dp[i-1][j], dp[i][j-1]);
        }
    }

    int i = n, j = m;
    string res;
    while (i >= 1 || j >= 1) {
        if (j == 0) res.push_back(s[i--]);
        else if (i == 0) res.push_back(t[j--]);
        else if (s[i] == t[j]) res.push_back(s[i]), i--, j--;
        else {
            if (dp[i-1][j] > dp[i][j-1]) res.push_back(s[i--]);
            else res.push_back(t[j--]);
        }
    }

    reverse(res.begin(), res.end());
    cout << res << endl;
}
  
