// https://leetcode.com/problems/count-pairs-of-points-with-distance-k/
// Find pair such that (x1^x2) + (y1^y2) = k;

void solve() {
    int n, k; cin >> n >> k;
    vpii a(n); for (auto &i: a) cin >> i.first >> i.second;
    map<pair<int, int>, int> dp;
    int ans = 0;
    for (auto &i: a) {
        int x1 = i.first, y1 = i.second;
        for (int X = 0; X <= k; X++) {
            int Y = k-X;
            int x2 = X^x1, y2 = Y^y1;
            ans += dp[{x2, y2}];
        }
        dp[{x1, y1}]++;
    }
    cout << ans << endl;
}
  
