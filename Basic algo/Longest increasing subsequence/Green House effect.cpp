int lis(vi a) {
    vi tail;
    for (int i: a) {
        auto it = upper_bound(tail.begin(), tail.end(), i);
        if (it == tail.end()) tail.pb(i);
        else *it = i;
    }
    return (int) tail.size();
}

void solve() {
    int n, m; cin >> n >> m;
    vi a;
    for (int i = 0; i < n; i++) {
        int p; double x; cin >> p >> x;
        a.pb(p);
    }
    cout << n-lis(a) << endl;
}

